package com.shagui.sdc.enums;

/**
 * Enum representing different types of metric validations.
 * 
 * <ul>
 * <li>GT - Greater Than</li>
 * <li>LT - Less Than</li>
 * <li>EQ - Equal</li>
 * <li>NEQ - Not Equal</li>
 * <li>GTE - Greater Than or Equal</li>
 * <li>LTE - Less Than or Equal</li>
 * <li>NA - Not Applicable</li>
 * </ul>
 */
public enum MetricValidation {
	GT, LT, EQ, NEQ, GTE, LTE, NA;
}
